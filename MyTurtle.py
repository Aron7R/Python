import turtle

screen = turtle.Screen()
screen.title("Learning Turtle")
screen.bgcolor("grey")

pen = turtle.Turtle()
pen.pensize(3)
pen.color("cyan")



#Right Angle triangle
pen.left(90)
pen.forward(100)
pen.right(120)
pen.forward(200)
pen.right(150)
pen.forward(180)






turtle.done()